idade_atleta = int(input("Digite a sua idade: "))

if (idade_atleta >= 5 and idade_atleta <= 10):
    print("Você está na categoria infantil")
if (idade_atleta >= 11 and idade_atleta <= 15):
    print("Você está na categoria Juvenil")
if (idade_atleta >= 16 and idade_atleta <= 20):
    print("Você está na categoria Júnior")
if (idade_atleta >= 21 and idade_atleta <= 25):
    print("Você está na categoria profissional")
